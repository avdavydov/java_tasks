    static String convertIntegerToRoman(int arabic){
        final String s = roman[arabic];
        return s;
    }

    static int convertRomanToInteger(String romanNumeral){
        return Arrays.asList(roman).indexOf(romanNumeral);
    }

     static int do_calc(int number1, int number2, char operation) {
        int result = 0;
        switch (operation) {
            case '+':
                result = number1 + number2;
                break;
            case '-':
                result = number1 - number2;
                break;
            case '*':
                result = number1 * number2;
                break;
            case '/':
                result = number1 / number2;
                break;

        }
        return result;
    }

